//字符倒转函数,数组作为参数传递，实则为指针传递不会出现所谓值传递的变量作用域问题。
void reverse(char s[])
{
    int i, j, c, n=0;
    while(s[n]!='\0')
        n++;
    /*i<j的判断是必须的，若没有，则会又将原本置换的部分又置换回去，实则没变*/
    for(i=0,j=n-1; i<j; i++,j--)
    {
        c = s[i];
        s[i] = s[j];
        s[j] = c;
    }
}

